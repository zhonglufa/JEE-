-- 验证课程数据
SELECT '课程数据统计' as info, COUNT(*) as count FROM courses;

-- 验证成绩数据
SELECT '成绩数据统计' as info, COUNT(*) as count FROM grades;

-- 验证成绩分布
SELECT 
    '成绩分布统计' as info,
    COUNT(*) as total_records,
    AVG(score) as average_score,
    MAX(score) as max_score,
    MIN(score) as min_score,
    COUNT(DISTINCT student_id) as unique_students,
    COUNT(DISTINCT course_id) as unique_courses
FROM grades;

-- 验证学生性别分布
SELECT 
    '学生性别分布' as info,
    gender,
    COUNT(*) as count
FROM students 
GROUP BY gender;

-- 验证教师课程分布
SELECT 
    '教师课程分布' as info,
    teacher,
    COUNT(*) as course_count
FROM courses 
GROUP BY teacher;

